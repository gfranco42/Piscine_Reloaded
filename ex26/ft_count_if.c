int		ft_count_if(char **tab, int(*f)(char*))
{
	int		a;
	int		count;

	a = 0;
	count = 0;
	while(tab[a] != 0)
	{
		if(f(tab[a]) == 1)
			count++;
		a++;
	}
	return(count);
}
