#include <unistd.h>

void	ft_putchar(char g)
{
	write(1, &g, 1);
}

void ft_print_numbers(void)
{
	int		g;
	g = 48;
	
	while (g <= 57)
	{
		ft_putchar(g);
		g = g + 1;
	}
}
